package sample;

public class PairInt {

    public int a;
    public int b;

    public PairInt(){
        a = 0;
        b = 0;
    }

    public PairInt(int a, int b){
        this.a = a;
        this.b = b;
    }

    public void setA(int a){
        this.a = a;
    }

    public void setB(int a){
        this.b = a;
    }
}